			<p>OBJECTIONS</p>
			<p>1. In its view the development is unsustainable; the only source of potential income actually tied to the land is the growing and sale of fresh produce. Other aspects of the business, namely the provision of music therapy sessions and hiking sessions do not require association with a parcel of land in the countryside.</p>
			<p>2. The development is outside the LDP and granting permission would be unfair and inequitable to other applications for dwellings that require the meeting of rigorous sustainability criteria prior to any application being granted as opposed to the requirement to prove sustainability for One Planet developments after permission has been granted.</p>
			<p>3. The various business elements proposed in this development would generate road traffic and footfall that pose safety concerns on the narrow country lane serving the property and spaces allocated for parking within the site are inadequate.</p>
			<p>4. The development would be visible from Dinefwr Park visited by tens of thousands of visitors each year.</p>
			<p>5. Question mark over water and electricity supplies on the site.</p>
