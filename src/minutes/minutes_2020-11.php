<p>8. The Remembrance Service had been held on Sunday 8th November, due to Covid 19 this had involved a procession to, and brief ceremony at, the Cenotaph as the Church remained closed to the public. Cllr. Alun Davies was thanked for clearing around the Monument and the police for providing traffic management.</p>
<p>It was suggested that a tree overhanging the monument should be cut down as it was contributing to the build-up of moss and algae and general damp conditions not favourable to the preservation of the Monument. Cllr. J. Davies would ask the landowner for permission to fell the tree.</p>
<p>Discussions were held over the possibility of creating a footpath within the field between the Church and monument but this was not considered feasible. It was felt that the current arrangements for a service that was held once a year, with the police in attendance to monitor traffic, worked very well.</p>
<p>It was proposed by Cllr. Alun Davies, seconded by Cllr. W. Loynton and unanimously agreed that a £100 donation be made to the Royal British Legion Poppy Appeal.						<em>Clerk</em></p>


