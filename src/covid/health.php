<h5>Pharmacies</h5>


<p>
	<strong>General Information</strong><br>
	During this unprecedented time, we can all help to support our pharmacists and their teams by following these five steps:
</p>


<ol>
	<li>Do not visit a pharmacy if you or someone you live with has a temperature or a new cough</li>
	<li>Order your prescriptions 7 days before you need them to give the pharmacy time to help everyone.</li>
	<li>Put your phone number on your prescription. Wait for the pharmacy to call you. The pharmacy will phone you when your prescription is ready to pick up. Do not call the pharmacy unless it is very urgent.</li>
	<li>If you are staying at home because you have a new cough or temperature get friends or family to pick up your prescriptions or speak to your community pharmacy to see if they can help.</li>
	<li>If you are not ill, offer to pick up prescriptions for any friends and family who are ill. You will need their name and address to be able to pick up their prescription.</li>
</ol>

