<h4>Shielding</h4>
<p><a href="https://gov.wales/people-extremely-vulnerable-covid-19-who-have-been-shielding-faqs">Chief Medical Officer letter to vulnerable people</a></p>

<p><strong>Carmarthenshire County Council</strong><br>We have been working hard to put arrangements in place to make sure support is available locally for people who are shielding. If residents do not have any family or friends that can help, or any local community groups that can support them, then they should contact the council.</p>
<p>We can arrange for food parcels to be delivered directly to their doorstep, and we can help supply and organise delivery of medicines where required. We are also in touch with a number of voluntary groups who can provide care and support for those who might be feeling very isolated.</p>
<p>You can help by directing residents to call us on 01267 234567 if they need any support, our telephone lines are open seven days a week between 8.30am and 6pm.</p>
<p>The list of people in the extremely vulnerable category was compiled by the NHS and unfortunately, we are unable to share this with you due to data protection regulations as it contains extremely sensitive personal information.</p>
