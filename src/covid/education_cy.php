<h4>Help for Kids at Home Schooling</h4>
<p>The following details a number of FREE online education resources that are available for both children and adults alike:</p>

<h5><a href="https://www.khanacademy.org">Khan Academy</a></h5>
<p>Especially good for maths and computing for all ages but other subjects at Secondary level. Note this uses the U.S. grade system but it’s mostly common material. The app can be downloaded on tablets and smartphones.</p>

<h5><a href="http://www.bbc.co.uk/learning/coursesearch/">BBC Learning</a></h5>
<p>This site is old and no longer updated and yet there’s so much still available, from language learning to BBC Bitesize for revision. No TV licence required except for content on BBC iPlayer.</p>

<h5><a href="https://www.futurelearn.com">FutureLearn</a></h5>
<p>Free to access 100s of courses, only pay to upgrade if you need a certificate in your name (own account from age 14+ but younger learners can use a parent account).</p>

<h5><a href="https://www.senecalearning.com">Seneca</a></h5>
<p>For those revising at GCSE or A level. Tons of free revision content. Paid access to higher level material.</p>

<h5><a href="https://www.open.edu/openlearn/">OpenLearn</a></h5>
<p>Free taster courses aimed at those considering Open University but everyone can access it. Adult level, but some e.g. nature and environment courses could well be of interest to young people.</p>

<h5><a href="https://blockly.games">Blockly</a></h5>
<p>Learn computer programming skills – fun and free.</p>

<h5><a href="https://scratch.mit.edu/explore/projects/games/">Scratch</a></h5>
<p>Creative computer programming</p>

<h5><a href="https://ed.ted.com">Ted Ed</a></h5>
<p>All sorts of engaging educational videos</p>

<h5><a href="https://www.natgeokids.com/uk/">National Geographic Kids</a></h5>
<p>Activities and quizzes for younger kids</p>

<h5><a href="https://www.duolingo.com">Duolingo</a></h5>
<p>Learn languages free. An app is also available to download for tablets and smartphones</p>

<h5><a href="https://mysteryscience.com">Mystery Science</a></h5>
<p>Free science lessons</p>

<h5><a href="https://thekidshouldseethis.com">The Kids Should See This</a></h5>
<p>Wide range of cool educational videos</p>

<h5><a href="https://thecrashcourse.com">Crash Course</a></h5>
<p>You Tube videos on many subjects</p>

<h5><a href="https://m.youtube.com/user/crashcoursekids">Crash Course Kids</a></h5>
<p>As above for a younger audience</p>

<h5><a href="https://www.crestawards.org">Crest Awards</a></h5>
<p>Science awards you can complete from home</p>


<h5><a href="https://idea.org.uk">iDEA Awards</a></h5>
<p>Digital enterprise award scheme you can complete online</p>

<h5><a href="https://www.pawprintbadges.co.uk">Paw Print Badges</a></h5>
<p>Free challenge packs and other downloads. Many activities can be completed indoors. Badges cost but are optional</p>

<h5><a href="https://www.tinkercad.com">Tinkercad</a></h5>
<p>All kinds of making</p>

<h5><a href="https://www.prodigygame.com">Prodigy Maths</a></h5>
<p>Is in U.S. grades, but good for UK Primary age</p>

<h5><a href="https://www.bbc.co.uk/cbeebies/radio">Cbeebies Radio</a></h5>
<p>Listening activities for the younger ones</p>

<h5><a href="https://www.woodlandtrust.org.uk/blog/2020/03/nature-detectives/">Nature Detectives</a></h5>
<p>A lot of these can be done in a garden, or if you can get to a remote forest location!</p>

<h5><a href="https://www.britishcouncil.org/school-resources/find">British Council</a></h5>
<p>Resources for English language learning</p>

<h5><a href="https://www.oxfordowl.co.uk/for-home/">Oxford Owl for Home</a></h5>
<p>Lots of free resources for Primary age</p>


<h5><a href="https://www.bighistoryproject.com/home">Big History Project</a></h5>
<p>Aimed at Secondary age. Multi disciplinary activities</p>

<h5><a href="https://world-geography-games.com/world.html">Geography Games</a></h5>
<p>Geography gaming!</p>

<h5><a href="https://www.bbc.co.uk/cbbc/joinin/about-blue-peter-badges">Blue Peter Badges</a></h5>
<p>If you have a stamp and a nearby post box</p>

<h5><a href="https://www.facebook.com/artfulparent/">The Artful Parent</a></h5>
<p>Good, free art activities</p>

<h5><a href="https://www.redtedart.com">Red Ted Art</a></h5>
<p>Easy arts and crafts for little ones</p>

<h5><a href="https://theimaginationtree.com">The Imagination Tree</a></h5>
<p>Creative art and craft activities for the very youngest</p>

<h5><a href="https://toytheater.com/">Toy Theater</a></h5>
<p>Educational online games</p>

<h5><a href="https://www.dkfindout.com/uk/">DK Find Out</a></h5>
<p>Activities and quizzes</p>

<h5><a href="https://www.twinkl.co.uk">Twinkl</a></h5>
<p>This is more for printouts, and usually at a fee, but they are offering a month of free access to parents in the event of school closures</p>