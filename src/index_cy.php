<p>Yn dilyn cystudd blin bu farw un o gymwynaswyr yr ardal J J Jim Davies, Manordeilo ar 12 Tachwedd yn 79 mlwydd oed. Yn enedigol o fferm Parcnewydd, Llanwrda bu’n amaethu am ddegawdau ar fferm Glanrhyd, Manordeilo tan ei ymddeoliad. Cofir amdano fel cynghorydd ar Fwrdeistref Dinefwr, yn Gyngor Sir Gaerfyrddin am ddeng mlynedd ar hugain ac yn gynghorydd cymuned ym Manordeilo a Salem. Roedd yn Ynad Heddwch am ugain mlynedd, bu’n Faer Bwrdeistref Dinefwr yn 1994/95 a derbyniodd yr MBE am ei wasanaeth clodwiw yn 2014. Roedd yn gymeriad addfwyn yn llawn hiwmor gyda diddordeb angerddol yn ei fro a’i phobol.</p>
<p>Bu’r angladd ar 18 Tachwedd yn Amlosgfa Llanelli gyda gwasanaeth o ddiolchgarwch am ei fywyd i ddilyn yn Eglwys Sant Paul, Cwmifor lle talwyd teyrnged iddo gan ei ffrind gydol oes y Parchedig Roger Thomas, ei ferch hynaf Catherine a David Watkins ffrind arall. Cynorthwywyd yn yr oedfa gan y Parchedig Jeffrey Thomas gyda Mair Griffith yn cyfeilio wrth yr organ.</p>
