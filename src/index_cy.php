<div id="id_second" class="js-tabcontent">
<p>Following a long illness one of the benefactors of the area, J J Jim Davies Manordeilo, died on 12 November at 79 years of age. A native of Parcnewydd Farm, Llanwrda, he was farming for decades on Glanrhyd Farm, Manordeilo until his retirement. He is remembered as a Councillor for Dinefwr Borough, for Carmarthenshire County Council for thirty years and as a community councillor in Manordeilo and Salem. He was a Justice of the Peace for twenty years, he was Mayor of the Dinefwr Borough in 1994/95 and received the MBE in 2014 for his commendable service in the community. He was a gentle, humorous character with a passionate interest in his locality and its people.</p>
<p>The funeral took place on 18 November at Llanelli Crematorium with a service to give thanks for his life to follow at St Paul's Church, Cwmifor where tributes were paid by his lifelong friend the Reverend Roger Thomas, his eldest daughter Catherine and David Watkins another friend. The Reverend Jeffrey Thomas assisted at the service with Mair Griffith at the organ.</p>
<p>We sympathised with his wife Jill, the girls, Catherine, Carys and Rhian, the grandsons and his brother Tudor in their sad loss.</p>
